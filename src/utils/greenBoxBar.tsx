export const greenBoxLevel = (lang: string) => {
     switch (lang) {
          case 'React':
               return 9
          case 'JavaScript':
               return 8
          case 'TypeScript':
               return 7
          case 'Python':
               return 5
          case 'NodeJS':
               return 6
          case 'Express':
               return 5
          case 'Design':
               return 9
          case 'SQL':
               return 7
          case 'MongoDB':
               return 4
          case 'Dart':
               return 2
          case 'Flutter':
               return 3
          case 'GIT':
               return 6
          default:
               break
     }
}